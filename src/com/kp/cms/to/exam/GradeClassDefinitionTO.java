package com.kp.cms.to.exam;

public class GradeClassDefinitionTO {

	
	private String fromPercentage;
	private String toPercentage;
	private String grade;
	private String interpretation;
	private String resultClass;
	private String gradePoint;
	
	private String courseName;
	private String numberOfSubject;
	
	public String getFromPercentage() {
		return fromPercentage;
	}
	public void setFromPercentage(String fromPercentage) {
		this.fromPercentage = fromPercentage;
	}
	public String getToPercentage() {
		return toPercentage;
	}
	public void setToPercentage(String toPercentage) {
		this.toPercentage = toPercentage;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getInterpretation() {
		return interpretation;
	}
	public void setInterpretation(String interpretation) {
		this.interpretation = interpretation;
	}
	public String getResultClass() {
		return resultClass;
	}
	public void setResultClass(String resultClass) {
		this.resultClass = resultClass;
	}
	public String getGradePoint() {
		return gradePoint;
	}
	public void setGradePoint(String gradePoint) {
		this.gradePoint = gradePoint;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getNumberOfSubject() {
		return numberOfSubject;
	}
	public void setNumberOfSubject(String numberOfSubject) {
		this.numberOfSubject = numberOfSubject;
	}
	
	
}
