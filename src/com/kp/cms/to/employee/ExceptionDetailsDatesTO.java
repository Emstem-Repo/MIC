package com.kp.cms.to.employee;


public class ExceptionDetailsDatesTO {
	
	private int id;
	private String date;
	private int exceptionDetailsId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getExceptionDetailsId() {
		return exceptionDetailsId;
	}
	public void setExceptionDetailsId(int exceptionDetailsId) {
		this.exceptionDetailsId = exceptionDetailsId;
	}

}
