package com.kp.cms.to.reports;

public class AdmittedStudentsReportsTO {

	private String className;

	private String applicationNo;

	private String applicantName;

	private String gender;

	private String prerequisitePercentage;

	private String slNo;

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getApplicationNo() {
		return applicationNo;
	}

	public void setApplicationNo(String applicationNo) {
		this.applicationNo = applicationNo;
	}

	public String getApplicantName() {
		return applicantName;
	}

	public void setApplicantName(String applicantName) {
		this.applicantName = applicantName;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getPrerequisitePercentage() {
		return prerequisitePercentage;
	}

	public void setPrerequisitePercentage(String prerequisitePercentage) {
		this.prerequisitePercentage = prerequisitePercentage;
	}

	public String getSlNo() {
		return slNo;
	}

	public void setSlNo(String slNo) {
		this.slNo = slNo;
	}

}
