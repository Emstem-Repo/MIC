package com.kp.cms.to.admin;


public class CommonTemplateTO {
	
	private String subjectName;
	private String marks;
	private String subjectPart;
	private String maxMarks;
	private String firstYearMarks;
	private String minMarks;
	
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getMarks() {
		return marks;
	}
	public void setMarks(String marks) {
		this.marks = marks;
	}
	public String getSubjectPart() {
		return subjectPart;
	}
	public void setSubjectPart(String subjectPart) {
		this.subjectPart = subjectPart;
	}
	public String getMaxMarks() {
		return maxMarks;
	}
	public void setMaxMarks(String maxMarks) {
		this.maxMarks = maxMarks;
	}
	public String getFirstYearMarks() {
		return firstYearMarks;
	}
	public void setFirstYearMarks(String firstYearMarks) {
		this.firstYearMarks = firstYearMarks;
	}
	public String getMinMarks() {
		return minMarks;
	}
	public void setMinMarks(String minMarks) {
		this.minMarks = minMarks;
	}
	
}
